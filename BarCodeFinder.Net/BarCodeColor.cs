namespace BarCodeFinder
{
    /// <summary>
    /// Defines the color of a section of a <see cref="BarCode"/>.
    /// </summary>
    public enum BarCodeColor : int
    {
        /// <summary>
        /// Red.
        /// </summary>
        Red = (int)'R',

        /// <summary>
        /// Green.
        /// </summary>
        Green = (int)'G',

        /// <summary>
        /// Blue.
        /// </summary>
        Blue = (int)'B'
    }
}
